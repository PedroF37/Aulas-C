#include <stdio.h>
#include <stdlib.h>


/*


                                        Aula 3
                                        
                               Incremento diferente de 1

*/

int main()
{
    int i;
    
    // imprimir números ímpares de 1 até 100.
    for (i = 1; i <= 99; i += 2)
        printf("%d ", i);
    
    putchar('\n');
    
    
    return(0);
}
