#include <stdio.h>
#include <stdlib.h>


/*


                                            Aula 1
                                            
                                     Estrutura de repetição: PARA (for)

*/

int main()
{
    int i = 0;
    
    for (i = 0; i <= 10; i++)
        printf("%d ", i);
    
    putchar('\n');
    
    
    return(0);
}
