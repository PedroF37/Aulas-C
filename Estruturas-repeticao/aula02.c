#include <stdio.h>
#include <stdlib.h>


/*


                                            Aula 2
                                            
                            Estrutura de repetição: PARA (for) decrescente

*/

int main()
{
    int i = 10;
    
    for (i = 10; i >= 0; i--)
        printf("%d ", i);
    
    putchar('\n');
    
    
    return(0);
}
